package entities;

public class Product {
	
	//Attributes
	private String name;
	private Double price;
	
	
	//Constructors
	public Product(String name, Double price) {
		this.name = name;
		this.price = price;
	}
	
	//Getters
	public String getName() {
		return name;
	}
	
	public Double getPrice() {
		return price;
	}

	
	
	
	
	
}
